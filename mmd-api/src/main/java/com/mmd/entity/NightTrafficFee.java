package com.mmd.entity;

public class NightTrafficFee {
	
	private int id;
	private int dot;
	private double fee;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getDot() {
		return dot;
	}
	public void setDot(int dot) {
		this.dot = dot;
	}
	public double getFee() {
		return fee;
	}
	public void setFee(double fee) {
		this.fee = fee;
	}
	

}
